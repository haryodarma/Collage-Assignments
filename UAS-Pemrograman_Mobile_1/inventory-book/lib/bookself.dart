class BookSelf {
  String _tittle = '';
  String _writer = '';
  int _stock = 0;


  set tittle(String value) => _tittle = value;
  String get tittle => _tittle;

  set writer(String value) => _writer = value;
  String get writer => _writer;

  set stock(int value) => _stock = value;
  int get stock => _stock;
}


